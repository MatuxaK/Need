-- Выбрать одну запись (TOP 1) с названием производителя, модели и датой выпуска
SELECT TOP 1 p.Название AS Производитель, m.Название AS Модель, m.Дата_выпуска
-- Из таблицы моделей ПЭВМ (сокращение 'm')
FROM Модели_ПЭВМ m
-- Соединить с таблицей поставок по совпадению ID модели
JOIN Поставки post ON m.ID = post.ID_модели
-- Соединить с таблицей производителей по совпадению ID производителя
JOIN Производители p ON post.ID_производителя = p.ID
-- Где тип процессора содержит 'i5' (LIKE с % означает "содержит")
WHERE m.Тип_процессора LIKE '%i5%'
-- Сортировать по дате выпуска в обратном порядке (новые сначала)
ORDER BY m.Дата_выпуска DESC;

-- Выбрать одну запись с названием модели, частотой и городом
SELECT TOP 1 m.Название AS Модель, m.Тактовая_частота, p.Город
-- Из таблицы моделей
FROM Модели_ПЭВМ m
-- Соединить с поставками
JOIN Поставки post ON m.ID = post.ID_модели
-- Соединить с производителями
JOIN Производители p ON post.ID_производителя = p.ID
-- Где город производителя - Тайбэй
WHERE p.Город = 'Тайбэй'
-- Сортировать по частоте (высокие значения сначала)
ORDER BY m.Тактовая_частота DESC;

-- Выбрать одного продавца с суммой всех его продаж
SELECT TOP 1 pr.Название AS Продавец, SUM(post.Цена_партии) AS Общий_объем_продаж
-- Из таблицы поставок
FROM Поставки post
-- Соединить с таблицей продавцов
JOIN Продавцы pr ON post.ID_продавца = pr.ID
-- Группировать по названию продавца (для агрегации SUM)
GROUP BY pr.Название
-- Сортировать по сумме продаж (большие суммы сначала)
ORDER BY Общий_объем_продаж DESC;

-- Выбрать производителя, модель и дату выпуска
SELECT p.Название AS Производитель, m.Название AS Модель, m.Дата_выпуска
-- Из таблицы моделей
FROM Модели_ПЭВМ m
-- Соединить с поставками
JOIN Поставки post ON m.ID = post.ID_модели
-- Соединить с производителями
JOIN Производители p ON post.ID_производителя = p.ID
-- Где тип процессора содержит 'Ryzen'
WHERE m.Тип_процессора LIKE '%Ryzen%'
-- Сортировать по дате выпуска (новые сначала)
ORDER BY m.Дата_выпуска DESC
-- Пропустить 0 строк, взять следующую 1 строку (аналог TOP 1)
OFFSET 0 ROWS FETCH NEXT 1 ROWS ONLY;